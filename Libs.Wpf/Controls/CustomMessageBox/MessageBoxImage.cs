    /// </summary>
    None = 0,

    /// <summary>
    ///     Displays the information image.
    /// </summary>
    Asterisk = 1,

    /// <summary>
    ///     Displays an error image.
    /// </summary>
    Error = 4,

    /// <summary>
    ///     Displays the exclamation image.
    /// </summary>
    Exclamation = 2,

    /// <summary>
    ///     Displays an error image.
    /// </summary>
    Hand = 4,

    /// <summary>
    ///     Displays the information image.
    /// </summary>
    Information = 1,

    /// <summary>
    ///     Displays the question image.
    /// </summary>
    Question = 3,

    /// <summary>
    ///     Displays an error image.
    /// </summary>
    Stop = 4,

    /// <summary>
    ///     Displays the exclamation image.
    /// </summary>
    Warning = 2
}
